    private void lblExportMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblExportMouseClicked
        if (!bill.getStatus())
            JOptionPane.showMessageDialog(this, "Bạn không thể xuất hoá đơn chưa thanh toán");
        else {
            try {
                ExportBill eb = new ExportBill(parent, true, dbUtil, bill);
            } catch (ParseException ex) {
                Logger.getLogger(PnlBill.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblExportMouseClicked

    private void tblBillMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBillMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON3 && !Common.isNullOrEmpty(bill)) { //right click
            jPopupMenu1.show(tblBill, evt.getX(), evt.getY());
        }
        if (bill.getStatus()) jMenuCheckOut.setVisible(false);
        else jMenuCheckOut.setVisible(true);
    }//GEN-LAST:event_tblBillMouseClicked

    private void jMenuCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuCheckOutActionPerformed
        try {
            bill.setStatus(true);

            Map<String, Object> billCheckout = billDao.update(bill);

            if ((boolean) billCheckout.get("status") == true) {
                JOptionPane.showMessageDialog(this, billCheckout.get("message"));
                loading(null, null, null);
            } 
            else 
                JOptionPane.showMessageDialog(this, billCheckout.get("message"));

        } catch (HeadlessException | NumberFormatException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jMenuCheckOutActionPerformed

