    private void loading(Bill newBill, String dateStart, String dateEnd) {
        bill = null;
        tblBill.removeAll();
        bills = billDao.getAll(newBill, dateStart, dateEnd);

        String columns[] = {"ID", "Nhân viên", "Bàn", "Tổng tiền", "Trạng thái", "Thời gian"};
        DefaultTableModel dtm = new DefaultTableModel(columns, 0);
        long allTotalPrice = 0;

        if (!Common.isNullOrEmpty(bills)) {
            for (Bill obj : bills) {
                dtm.addRow(new Object[]{obj.getId(), obj.getUser_name(), obj.getTable_name(), NumberFormat.getInstance().format(obj.getTotal_price()), obj.getStatus() ? "Đã thanh toán" : "Chưa thanh toán", obj.getCreated_at()});
                if (obj.getStatus()) 
                    allTotalPrice += obj.getTotal_price();
            }

            tblBill.getSelectionModel().addListSelectionListener((ListSelectionEvent lse) -> {
                int position = tblBill.getSelectedRow();
                if (position >= 0) 
                    bill = bills.get(position);

            });
            txtTotalBill.setText(NumberFormat.getInstance().format(allTotalPrice));
            tblBill.changeSelection(0, 0, false, false);
        }

        tblBill.setModel(dtm);
    }

