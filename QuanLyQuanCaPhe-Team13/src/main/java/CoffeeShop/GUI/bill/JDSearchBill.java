    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        //set default
        txtUserId.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(Color.BLACK),
                    BorderFactory.createEmptyBorder(5, 8, 5, 8)));
        lblUserId.setForeground(Color.BLACK);
        txtTimeStart.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(Color.BLACK),
                    BorderFactory.createEmptyBorder(5, 8, 5, 8)));
        txtTimeEnd.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(Color.BLACK),
                    BorderFactory.createEmptyBorder(5, 8, 5, 8)));
        lblTimeError1.setVisible(false);
        lblTimeError.setVisible(false);
        lblIdError.setVisible(false);
        
        bill = new Bill();
        boolean valid = true;
        
        String dateStart = null;
        String dateEnd = null;
        
        if (!Common.isNullOrEmpty(txtTimeStart.getText())) {
            if (!isValidDate(txtTimeStart.getText())) {
                valid = false;                
                txtTimeStart.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(Color.RED),
                    BorderFactory.createEmptyBorder(5, 8, 5, 8)));
                lblTimeError.setVisible(true);
            }
            else 
                dateStart = txtTimeStart.getText();
        }
        
        if (!Common.isNullOrEmpty(txtTimeEnd.getText())) {
            if (!isValidDate(txtTimeEnd.getText())) {
                valid = false;  
                txtTimeEnd.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(Color.RED),
                    BorderFactory.createEmptyBorder(5, 8, 5, 8))); 
                lblTimeError1.setVisible(true);
            }
            else 
                dateEnd = txtTimeEnd.getText();
        }

        if (!Common.isNullOrEmpty(txtUserId.getText())) {
            if (!Common.isInteger(txtUserId.getText())) {
                valid = false;  
                txtUserId.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(Color.RED),
                    BorderFactory.createEmptyBorder(5, 8, 5, 8))); 
                lblIdError.setVisible(true);
            }
            else 
                bill.setUser_id(Integer.valueOf(txtUserId.getText().trim()));
        }
        
        if (valid) {       
            if (!Common.isNullOrEmpty(txtTableName.getText().trim())) 
                bill.setTable_name(txtTableName.getText().trim());

            if (!"Không chọn".equals(cboStatus.getSelectedItem().toString())) 
                bill.setStatus("Đã thanh toán".equals(cboStatus.getSelectedItem().toString()));

            callback.actionBillSearch(bill, dateStart, dateEnd);
            dispose();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

