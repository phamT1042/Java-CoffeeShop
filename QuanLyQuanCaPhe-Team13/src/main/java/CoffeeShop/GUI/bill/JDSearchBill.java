    private static boolean isValidDate (String dateStr) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        sdf.setLenient(false);
        try {
            sdf.parse(dateStr);
        } 
        catch (Exception e) {
            return false;
        }
        return true;       
    }

